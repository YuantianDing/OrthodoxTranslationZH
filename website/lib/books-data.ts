export const books = Object.fromEntries(
  books_raw.map((book) => [
    transliterate(book.title[0] + "." + book.authors.map(a => a[0]).join(".")),
    book,
  ]),  
)

export function retrieve_book(id: string): Book | null {
  return books[id] || null
}

